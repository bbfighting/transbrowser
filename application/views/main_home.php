<h4>HumanIREZone:  Human Internal Ribosome Entry Zone database</h4>
<div style="font-family:'Droid Serif', Georgia, Times, serif, 微軟正黑體;text-align:justify;padding-right:100px;text-align:justify;">The translational regulation mechanisms of gene expression can be divided into two types: Cap-dependent and IRES-dependent. IRES (Internal Ribosome Entry Site), located in the 5’UTR region, is a cis-acting element which can attract the ribosome complexes to synthesize proteins. IRES is known to be involved in cell apoptosis and the regulation of oncogenes and growth factors. IREZone (Internal Ribosome Entry Zone), a region of multiple overlapping IRES structures, has been shown to have strong translational regulation capability. Therefore, knowing the information of IREZone or IRESs in the 5’UTR of a gene is very helpful for studying the translational regulation mechanism of that gene. In this study, we use the Scan-For-Matches software to identify the IRES in the 5’UTR region of each human mRNA transcript. Then we define IREZone as a region with at least three overlapping IRESs. Finally, we construct a database called HumanIREZone. For each transcript, our database deposits a figure showing the identified IREZone, ribosome binding patterns (using public translational profiling data) and uORFs (upstream open reading frames) in the 5’UTR of that transcript. When users input a human gene name, HumanIREZone will return IREZone information of all transcripts related to the input gene. By integrating information of IREZone, ribosome binding patterns, and uORFs, HumanIREZone database provides researchers a global picture about the translational regulation of the genes of interest.</div>
